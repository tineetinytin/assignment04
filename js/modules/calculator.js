const add = (x, y) => {
    let sum = x + y;
    return `The sum of two numbers is ${sum}.`;
};

const subtract = (x, y) => {
    let diff = x - y;
    return `The difference of two numbers is ${diff}.`;
};

const multiply = (x, y) => {
    let mult = x * y;
    return `The product of two numbers is ${mult}.`;
};

const divide = (x, y) => {
    let divd = x / y;
    return `The quotient of two numbers is ${divd}.`;
};
